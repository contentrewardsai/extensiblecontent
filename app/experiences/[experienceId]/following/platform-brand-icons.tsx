export type PlatformBrandIconSize = "sm" | "md";

const SIZE_MAP: Record<PlatformBrandIconSize, { tile: string; glyph: number }> = {
	sm: { tile: "h-6 w-6 rounded-md", glyph: 14 },
	md: { tile: "h-7 w-7 rounded-md", glyph: 16 },
};

function normalizeSlug(slug: string | undefined | null): string {
	return (slug ?? "").trim().toLowerCase();
}

export function PlatformBrandIcon({
	slug,
	name,
	size = "md",
	...rest
}: {
	slug: string | undefined | null;
	name?: string | null;
	size?: PlatformBrandIconSize;
} & Omit<SVGProps<HTMLDivElement>, "children">) {
	const key = normalizeSlug(slug);
	const glyph = GLYPHS[key];
	const style = TILE_STYLES[key] ?? { background: "#6b7280", color: "#ffffff" };
	const dims = SIZE_MAP[size];
	const label = name || slug || "platform";
	const initial = (name ?? slug ?? "?").trim().charAt(0).toUpperCase() || "?";

	return (
		<span
			role="img"
			aria-label={label}
			title={label}
			className={`${dims.tile} inline-grid place-items-center shrink-0 select-none`}
			style={{ background: style.background, color: style.color }}
			{...(rest as Record<string, unknown>)}
		>
			{glyph ? (
				<svg
					viewBox={glyph.viewBox ?? "0 0 24 24"}
					width={dims.glyph}
					height={dims.glyph}
					fill="currentColor"
					aria-hidden
				>
					<path d={glyph.path} />
				</svg>
			) : (
				<span className="text-2 font-semibold">{initial}</span>
			)}
		</span>
	);
}

const URL_TEMPLATES: Record<string, (handle: string) => string> = {
	tiktok: (h) => `https://www.tiktok.com/@${h}`,
	instagram: (h) => `https://www.instagram.com/${h}`,
	linkedin: (h) => `https://www.linkedin.com/in/${h}`,
	x: (h) => `https://x.com/${h}`,
	twitter: (h) => `https://twitter.com/${h}`,
	facebook: (h) => `https://www.facebook.com/${h}`,
	youtube: (h) => (h.startsWith("@") ? `https://www.youtube.com/${h}` : `https://www.youtube.com/@${h}`),
	threads: (h) => `https://www.threads.com/@${h}`,
	pinterest: (h) => `https://www.pinterest.com/${h}`,
	snapchat: (h) => `https://www.snapchat.com/add/${h}`,
	bluesky: (h) => `https://bsky.app/profile/${h}`,
	reddit: (h) => (h.startsWith("u/") ? `https://www.reddit.com/${h}` : `https://www.reddit.com/u/${h}`),
};

export function platformProfileUrl(slug: string | undefined | null, handle: string | undefined | null): string | null {
	const s = normalizeSlug(slug);
	const h = (handle ?? "").trim().replace(/^@+/, "");
	if (!h) return null;
	const fn = URL_TEMPLATES[s];
	return fn ? fn(h) : null;
}
